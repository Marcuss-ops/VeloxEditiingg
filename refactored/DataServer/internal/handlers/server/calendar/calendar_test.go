func TestCalendarAPI_UpdateCompletesQueuedJobWithoutDuplicate(t *testing.T) {
	_, q, r := setupCalendarTestEnv(t)

	create := incompleteAgentEvent()
	create.ScriptText = "Test script without clips."
	w := postJSON(t, r, "/api/v1/calendar/events", create)
	if w.Code != http.StatusCreated {
		t.Fatalf("create status: %d body=%s", w.Code, w.Body.String())
	}
	created := decodeEvent(t, w)

	update := store.CalendarEvent{
		StockFootage:      []store.VideoClip{{Type: "stock", URL: "https://example.com/stock1.mp4"}},
		InitialClips:      []store.VideoClip{{Type: "clip", URL: "https://example.com/intro.mp4"}},
		IntermediateClips: []store.VideoClip{{Type: "clip", URL: "https://example.com/middle.mp4"}},
		FinalClips:        []store.VideoClip{{Type: "clip", URL: "https://example.com/final.mp4"}},
	}
	w = putJSON(t, r, "/api/v1/calendar/events/"+created.ID, update)
	if w.Code != http.StatusOK {
		t.Fatalf("update status: %d body=%s", w.Code, w.Body.String())
	}
	updated := decodeEvent(t, w)
	if updated.Status != "queued" {
		t.Fatalf("expected queued after update, got %q", updated.Status)
	}
	if updated.JobID == "" {
		t.Fatal("expected job id after update")
	}

	stats, err := q.Stats(context.Background())
	if err != nil {
		t.Fatalf("queue stats: %v", err)
	}
	if stats["total"] != 1 {
		t.Fatalf("expected one queued job, got %v", stats)
	}

	job, err := q.GetJob(context.Background(), updated.JobID)
	if err != nil {
		t.Fatalf("get job: %v", err)
	}
	if job == nil {
		t.Fatal("expected job in queue")
	}
	if job.Payload["calendar_event_id"] != updated.ID {
		t.Fatalf("job not linked to event: %+v", job.Payload)
	}
}

func TestCalendarAPI_ExternalIDIsIdempotent(t *testing.T) {
	db, q, r := setupCalendarTestEnv(t)

	payload := fullAgentEvent()
	w := postJSON(t, r, "/api/v1/calendar/events", payload)
	if w.Code != http.StatusCreated {
		t.Fatalf("first create status: %d body=%s", w.Code, w.Body.String())
	}
	first := decodeEvent(t, w)

	w = postJSON(t, r, "/api/v1/calendar/events", payload)
	if w.Code != http.StatusOK {
		t.Fatalf("second create status: %d body=%s", w.Code, w.Body.String())
	}
	second := decodeEvent(t, w)

	if second.ID != first.ID {
		t.Fatalf("expected same event id, got %q and %q", first.ID, second.ID)
	}

	events, err := db.ListCalendarEvents(context.Background(), store.CalendarEventFilter{})
	if err != nil {
		t.Fatalf("list calendar events: %v", err)
	}
	if len(events) != 1 {
		t.Fatalf("expected one calendar event after idempotent create, got %d", len(events))
	}

	stats, err := q.Stats(context.Background())
	if err != nil {
		t.Fatalf("queue stats: %v", err)
	}
	if stats["total"] != 1 {
		t.Fatalf("expected one job after idempotent create, got %v", stats)
	}
}

