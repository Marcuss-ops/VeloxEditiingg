func setupCalendarTestEnv(t *testing.T) (*store.SQLiteStore, *queue.FileQueue, *gin.Engine) {
	t.Helper()
	gin.SetMode(gin.TestMode)

	dbPath := filepath.Join(t.TempDir(), "calendar.sqlite")
	db, err := store.NewSQLiteStore(dbPath)
	if err != nil {
		t.Fatalf("new store: %v", err)
	}
	t.Cleanup(func() { _ = db.Close() })

	q, err := queue.NewFileQueue(&queue.FileQueueConfig{DBStore: db, MaxRetries: 3})
	if err != nil {
		t.Fatalf("new queue: %v", err)
	}

	r := gin.New()
	v1 := r.Group("/api/v1")
	sched := NewCalendarScheduler(db, q)
	RegisterRoutes(v1, db, q, sched)

	return db, q, r
}

func postJSON(t *testing.T, r http.Handler, path string, payload any) *httptest.ResponseRecorder {
	t.Helper()
	body, err := json.Marshal(payload)
	if err != nil {
		t.Fatalf("marshal payload: %v", err)
	}
	w := httptest.NewRecorder()
	req := httptest.NewRequest(http.MethodPost, path, bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	r.ServeHTTP(w, req)
	return w
}

func putJSON(t *testing.T, r http.Handler, path string, payload any) *httptest.ResponseRecorder {
	t.Helper()
	body, err := json.Marshal(payload)
	if err != nil {
		t.Fatalf("marshal payload: %v", err)
	}
	w := httptest.NewRecorder()
	req := httptest.NewRequest(http.MethodPut, path, bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	r.ServeHTTP(w, req)
	return w
}

func decodeEvent(t *testing.T, body *httptest.ResponseRecorder) store.CalendarEvent {
	t.Helper()
	var event store.CalendarEvent
	if err := json.Unmarshal(body.Body.Bytes(), &event); err != nil {
		t.Fatalf("decode event: %v body=%s", err, body.Body.String())
	}
	return event
}

func decodeList(t *testing.T, body *httptest.ResponseRecorder) listResponse {
	t.Helper()
	var res listResponse
	if err := json.Unmarshal(body.Body.Bytes(), &res); err != nil {
		t.Fatalf("decode list: %v body=%s", err, body.Body.String())
	}
	return res
}

