func TestCalendarStore_PersistsFullEventPayload(t *testing.T) {
	db, _, _ := setupCalendarTestEnv(t)

	event := fullAgentEvent()
	if err := db.CreateCalendarEvent(context.Background(), &event); err != nil {
		t.Fatalf("create calendar event: %v", err)
	}

	got, err := db.GetCalendarEvent(context.Background(), event.ID)
	if err != nil {
		t.Fatalf("get calendar event: %v", err)
	}
	if got == nil {
		t.Fatal("expected persisted event")
	}
	if got.ExternalID != event.ExternalID {
		t.Fatalf("externalId mismatch: got %q want %q", got.ExternalID, event.ExternalID)
	}
	if got.Source != event.Source {
		t.Fatalf("source mismatch: got %q want %q", got.Source, event.Source)
	}
	if got.Title != event.Title || got.Date != event.Date || got.Month != event.Month || got.Year != event.Year {
		t.Fatalf("basic fields not preserved: %+v", got)
	}
	if got.YouTubeGroup != event.YouTubeGroup || got.Category != event.Category || got.ScriptText != event.ScriptText {
		t.Fatalf("metadata not preserved: %+v", got)
	}
	if len(got.Titles) != len(event.Titles) || got.Titles[0] != event.Titles[0] {
		t.Fatalf("titles not preserved: %+v", got.Titles)
	}
	if len(got.StockFootage) != 1 || got.StockFootage[0].URL != "https://example.com/stock1.mp4" {
		t.Fatalf("stock footage not preserved: %+v", got.StockFootage)
	}
	if len(got.InitialClips) != 1 || len(got.IntermediateClips) != 1 || len(got.FinalClips) != 1 {
		t.Fatalf("clip arrays not preserved: %+v", got)
	}
}

func TestCalendarAPI_CreateQueuesAndReturnsAgentFields(t *testing.T) {
	_, _, r := setupCalendarTestEnv(t)

	w := postJSON(t, r, "/api/v1/calendar/events", fullAgentEvent())
	if w.Code != http.StatusCreated {
		t.Fatalf("want status 201, got %d body=%s", w.Code, w.Body.String())
	}

	event := decodeEvent(t, w)
	if event.ID == "" || event.JobID == "" {
		t.Fatalf("expected event id and job id, got %+v", event)
	}
	if event.Status != "queued" {
		t.Fatalf("expected queued status, got %q", event.Status)
	}
	if event.JobStatus != string(queue.StatusPending) {
		t.Fatalf("expected pending jobStatus, got %q", event.JobStatus)
	}
	if event.QueueError != "" {
		t.Fatalf("expected no queue error, got %q", event.QueueError)
	}

	w = httptest.NewRecorder()
	now := time.Now().UTC()
	req := httptest.NewRequest(http.MethodGet, "/api/v1/calendar/events?month="+strconv.Itoa(int(now.Month()))+"&year="+strconv.Itoa(now.Year()), nil)
	r.ServeHTTP(w, req)
	if w.Code != http.StatusOK {
		t.Fatalf("want list 200, got %d body=%s", w.Code, w.Body.String())
	}
	res := decodeList(t, w)
	if res.Count != 1 || len(res.Events) != 1 {
		t.Fatalf("expected 1 event in list, got %+v", res)
	}
	got := res.Events[0]
	if got.ScriptText != fullAgentEvent().ScriptText {
		t.Fatalf("script text not returned: %+v", got)
	}
	if got.YouTubeGroup != fullAgentEvent().YouTubeGroup || got.Category != fullAgentEvent().Category {
		t.Fatalf("agent fields missing: %+v", got)
	}
	if got.JobID == "" || got.Status != "queued" {
		t.Fatalf("queued fields missing: %+v", got)
	}
}

