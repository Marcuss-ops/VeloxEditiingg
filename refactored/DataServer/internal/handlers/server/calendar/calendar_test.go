func TestCalendarAPI_StatusLifecycleAndOutputs(t *testing.T) {
	_, q, r := setupCalendarTestEnv(t)

	w := postJSON(t, r, "/api/v1/calendar/events", fullAgentEvent())
	if w.Code != http.StatusCreated {
		t.Fatalf("create status: %d body=%s", w.Code, w.Body.String())
	}
	event := decodeEvent(t, w)

	if err := q.UpdateJobFields(context.Background(), event.JobID, map[string]interface{}{
		"status":            string(queue.StatusProcessing),
		"master_video_path": "/tmp/output.mp4",
		"drive_url":         "https://drive.example.com/video",
	}); err != nil {
		t.Fatalf("set processing: %v", err)
	}

	w = httptest.NewRecorder()
	req := httptest.NewRequest(http.MethodGet, "/api/v1/calendar/events/"+event.ID, nil)
	r.ServeHTTP(w, req)
	if w.Code != http.StatusOK {
		t.Fatalf("get processing status: %d body=%s", w.Code, w.Body.String())
	}
	processing := decodeEvent(t, w)
	if processing.Status != "processing" {
		t.Fatalf("expected processing, got %q", processing.Status)
	}

	if err := q.UpdateJobFields(context.Background(), event.JobID, map[string]interface{}{
		"status":            string(queue.StatusCompleted),
		"master_video_path": "/tmp/output.mp4",
		"drive_url":         "https://drive.example.com/video",
	}); err != nil {
		t.Fatalf("set completed: %v", err)
	}

	w = httptest.NewRecorder()
	req = httptest.NewRequest(http.MethodGet, "/api/v1/calendar/events/"+event.ID, nil)
	r.ServeHTTP(w, req)
	if w.Code != http.StatusOK {
		t.Fatalf("get completed status: %d body=%s", w.Code, w.Body.String())
	}
	completed := decodeEvent(t, w)
	if completed.Status != "completed" {
		t.Fatalf("expected completed, got %q", completed.Status)
	}
	if completed.OutputVideoPath != "/tmp/output.mp4" {
		t.Fatalf("expected output video path, got %+v", completed)
	}
	if completed.OutputVideoURL != "https://drive.example.com/video" {
		t.Fatalf("expected output video url, got %+v", completed)
	}
	if completed.PublishStatus != "manual" {
		t.Fatalf("expected manual publish status, got %q", completed.PublishStatus)
	}
}
