func TestFormat_StringerInvariants(t *testing.T) {
	cases := []struct {
		in   Format
		want string
	}{
		{FormatUnknown, "unknown"},
		{FormatPNG, "png"},
		{FormatJPEG, "jpeg"},
		{FormatGIF, "gif"},
		{FormatWEBP, "webp"},
	}
	for _, tc := range cases {
		if got := tc.in.String(); got != tc.want {
			t.Errorf("Format(%d).String() = %q, want %q", int(tc.in), got, tc.want)
		}
	}
	if !FormatUnknown.IsZero() {
		t.Errorf("zero value FormatUnknown.IsZero() must be true")
	}
	if FormatPNG.IsZero() || FormatJPEG.IsZero() || FormatGIF.IsZero() || FormatWEBP.IsZero() {
		t.Errorf("only FormatUnknown should report IsZero()=true")
	}
}

func TestSentinelErrors(t *testing.T) {
	if ErrUnsupportedFormat.Error() != "images: unsupported format" {
		t.Errorf("ErrUnsupportedFormat mutated; got %q", ErrUnsupportedFormat.Error())
	}
	if ErrEmptyBuffer.Error() != "images: empty buffer" {
		t.Errorf("ErrEmptyBuffer mutated; got %q", ErrEmptyBuffer.Error())
	}
	if !errors.Is(ErrUnsupportedFormat, ErrUnsupportedFormat) {
		t.Errorf("errors.Is identity round-trip failed")
	}
	if errors.Is(ErrUnsupportedFormat, ErrEmptyBuffer) {
		t.Errorf("sentinel errors must not be Is()-equivalent")
	}
}

func TestDetectFormat_MagicBytes(t *testing.T) {
	tests := []struct {
		name string
		data []byte
		want Format
	}{
		{"png_full_sig", []byte{0x89, 'P', 'N', 'G', 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00}, FormatPNG},
		{"png_minimal", []byte{0x89, 'P', 'N', 'G'}, FormatPNG},
		{"jpeg_e0", []byte{0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10}, FormatJPEG},
		{"jpeg_e1", []byte{0xFF, 0xD8, 0xFF, 0xE1}, FormatJPEG},
		{"fdm_e0_short", []byte{0xFF, 0xD8, 0xFF}, FormatJPEG},
		{"gif87a", []byte("GIF87a....."), FormatGIF},
		{"gif89a", []byte("GIF89a....."), FormatGIF},
		{"webp_vp8", []byte("RIFF\x10\x00\x00\x00WEBPVP8 "), FormatWEBP},
		{"webp_vp8l", []byte("RIFF\x24\x00\x00\x00WEBPVP8L"), FormatWEBP},
		{"webp_vp8x", []byte("RIFF\x12\x00\x00\x00WEBPVP8X"), FormatWEBP},
		{"pdf", []byte("%PDF-1.7"), FormatUnknown},
		{"zip", []byte{0x50, 0x4B, 0x03, 0x04}, FormatUnknown},
		{"ascii", []byte("hello world"), FormatUnknown},
		{"empty", []byte{}, FormatUnknown},
		{"one", []byte{0x00}, FormatUnknown},
		{"fdm_only", []byte{0xFF, 0xD8}, FormatUnknown},
		{"gif_short", []byte{'G', 'I'}, FormatUnknown},
		{"webp_short", []byte("RIF"), FormatUnknown},
		{"png_lower_p", []byte{0x89, 'p', 'N', 'G'}, FormatUnknown},
		{"zero_buf", make([]byte, 0), FormatUnknown},
	}
	for _, tc := range tests {
		tc := tc
		t.Run(tc.name, func(t *testing.T) {
			if got := DetectFormat(tc.data); got != tc.want {
				t.Errorf("DetectFormat(data=%x) = %v, want %v", tc.data, got, tc.want)
			}
		})
	}
}

