func TestValidate_Bounds(t *testing.T) {
	cases := []struct {
		name       string
		dim        Dimension
		maxW, maxH int
		wantErr    bool
	}{
		{"valid_standard", Dimension{1920, 1080}, 4096, 4096, false},
		{"valid_no_max_w", Dimension{8000, 1080}, 0, 4096, false},
		{"valid_no_max_h", Dimension{1920, 8000}, 4096, 0, false},
		{"valid_no_max_either", Dimension{8000, 8000}, 0, 0, false},
		{"err_zero_w", Dimension{0, 100}, 4096, 4096, true},
		{"err_zero_h", Dimension{100, 0}, 4096, 4096, true},
		{"err_zero_both", Dimension{0, 0}, 4096, 4096, true},
		{"err_neg_w", Dimension{-1, 100}, 4096, 4096, true},
		{"err_neg_h", Dimension{100, -1}, 4096, 4096, true},
		{"err_over_w", Dimension{5000, 1080}, 4096, 4096, true},
		{"err_over_h", Dimension{1920, 5000}, 4096, 4096, true},
		{"err_over_both", Dimension{5000, 5000}, 4096, 4096, true},
	}
	for _, tc := range cases {
		tc := tc
		t.Run(tc.name, func(t *testing.T) {
			err := Validate(tc.dim, tc.maxW, tc.maxH)
			if tc.wantErr && err == nil {
				t.Errorf("Validate(%v, %d, %d): expected error, got nil", tc.dim, tc.maxW, tc.maxH)
			}
			if !tc.wantErr && err != nil {
				t.Errorf("Validate(%v, %d, %d): unexpected error %v", tc.dim, tc.maxW, tc.maxH, err)
			}
		})
	}
}

